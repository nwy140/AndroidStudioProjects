

fun show (name:String ):Unit { // unit allows only 1 value

    println ("name:"+name);

}

fun show(number:Int):Unit{

    println("number:"+number);
}

fun show(numberD:Double):Unit{

    println("numberD:"+numberD);
}




fun main (args:Array<String>) {

    show ("hussein");
    show (100);
    show (10.5);
    show(1000000);









}